using System;

namespace AssemblyCSharp
{
	/// <summary>
	/// Enum to represent each end of a range, which has two ends to it: one for the maximum value and one for the 
	/// minimum value. Respectively, MAXIMUM represents the end with the maximum value, while MINIMUM represents that
	/// with the minimum value.
	/// </summary>
	public enum BoundaryEnd
	{
		MAXIMUM, MINIMUM
	}
}

